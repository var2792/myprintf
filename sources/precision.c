#include "../includes/format_specifier.h"

int	f_precision(char **str, t_format *f)
{
	f->pr = **str;
	if (f->pl == -1)
		f->pl = 0;
	if (f->pr == '.')
	{
		(*str)++;
		return (1);
	}
	return (0);
}
