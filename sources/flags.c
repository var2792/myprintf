#include "../includes/format_specifier.h"

int		f_flags(char **str, t_format *f)
{
	f->fl = **str;
	if (f->pl == -1)
		f->pl = 0;
	if (f->fl == '-')
	{
		(*str)++;
		return (1);
	}
	if (f->fl == '0')
	{
		(*str)++;
		if (**str == '-')
		{
			f->fl = '-';
			(*str)++;
		}
		return (1);
	}
	return (0);
}
